import { spawnSync } from 'node:child_process'
import { closeSync, openSync, readFileSync } from 'node:fs'
import { readFile, rm, writeFile } from 'node:fs/promises'
import os from 'node:os'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const projectRoot = path.resolve(scriptDir, '..')
const outputPath = path.join(projectRoot, 'THIRD_PARTY_LICENSES.md')
const checkOnly = process.argv.includes('--check')
// Keep the generated inventory stable across Windows/Linux CI by excluding
// platform-specific optional binary packages from the source query.
const licenseQueryArgs = ['licenses', 'list', '--json', '--no-optional']

function resolvePnpmInvocation() {
  if (process.platform === 'win32') {
    return {
      command: process.env.ComSpec || 'cmd.exe',
      args: ['/d', '/s', '/c', 'pnpm.cmd'],
    }
  }

  const npmExecPath = process.env.npm_execpath

  if (npmExecPath && path.basename(npmExecPath).toLowerCase().includes('pnpm')) {
    return {
      command: process.execPath,
      args: [npmExecPath],
    }
  }

  return {
    command: 'pnpm',
    args: [],
  }
}

function runLicenseQuery() {
  const pnpm = resolvePnpmInvocation()
  const tempOutputPath = path.join(
    os.tmpdir(),
    `chill-vibe-licenses-${process.pid}-${Date.now()}.json`,
  )
  const stdoutFd = openSync(tempOutputPath, 'w')
  try {
    const result = spawnSync(pnpm.command, [...pnpm.args, ...licenseQueryArgs], {
      cwd: projectRoot,
      stdio: ['ignore', stdoutFd, 'inherit'],
    })

    if (result.error) {
      throw result.error
    }

    if (result.status !== 0) {
      throw new Error('Failed to query dependency licenses.')
    }

    return JSON.parse(readFileSync(tempOutputPath, 'utf8'))
  } finally {
    closeSync(stdoutFd)
    void rm(tempOutputPath, { force: true })
  }
}

function formatVersions(input) {
  const values = Array.isArray(input) ? input : [input]
  const versions = values
    .map((value) => String(value || '').trim())
    .filter(Boolean)
    .sort((left, right) => left.localeCompare(right))

  return versions.join(', ') || 'unknown'
}

function buildInventory(raw) {
  const licenseEntries = Object.entries(raw)
    .map(([license, packages]) => ({
      license,
      packages: [...packages]
        .map((pkg) => ({
          name: String(pkg.name || '').trim(),
          versions: formatVersions(pkg.versions),
          homepage: typeof pkg.homepage === 'string' ? pkg.homepage.trim() : '',
        }))
        .sort((left, right) => {
          const byName = left.name.localeCompare(right.name)
          return byName !== 0 ? byName : left.versions.localeCompare(right.versions)
        }),
    }))
    .sort((left, right) => left.license.localeCompare(right.license))

  const totalPackages = licenseEntries.reduce((sum, entry) => sum + entry.packages.length, 0)
  const lines = [
    '# Third-Party Dependency Inventory',
    '',
    'This file is generated by `pnpm legal:generate` from `pnpm licenses list --json --no-optional`.',
    'Do not edit manually.',
    '',
    '## Summary',
    '',
    `- Total packages: ${totalPackages}`,
    `- License families: ${licenseEntries.length}`,
    '',
    '## License Families',
    '',
  ]

  for (const entry of licenseEntries) {
    lines.push(`- \`${entry.license}\`: ${entry.packages.length} package${entry.packages.length === 1 ? '' : 's'}`)
  }

  lines.push('', '## Packages By License', '')

  for (const entry of licenseEntries) {
    lines.push(`### ${entry.license} (${entry.packages.length})`, '')

    for (const pkg of entry.packages) {
      const line = pkg.homepage
        ? `- \`${pkg.name}@${pkg.versions}\` - ${pkg.homepage}`
        : `- \`${pkg.name}@${pkg.versions}\``
      lines.push(line)
    }

    lines.push('')
  }

  return `${lines.join('\n')}\n`
}

async function verifyOutput(nextContent) {
  let currentContent = ''

  try {
    currentContent = await readFile(outputPath, 'utf8')
  } catch (error) {
    if (error && typeof error === 'object' && 'code' in error && error.code === 'ENOENT') {
      currentContent = ''
    } else {
      throw error
    }
  }

  if (currentContent !== nextContent) {
    throw new Error('THIRD_PARTY_LICENSES.md is out of date. Run `pnpm legal:generate` and commit the result.')
  }
}

async function main() {
  const raw = runLicenseQuery()
  const nextContent = buildInventory(raw)

  if (checkOnly) {
    await verifyOutput(nextContent)
    console.log('[legal] THIRD_PARTY_LICENSES.md is up to date.')
    return
  }

  await writeFile(outputPath, nextContent, 'utf8')
  console.log('[legal] Wrote THIRD_PARTY_LICENSES.md')
}

main().catch((error) => {
  console.error('[legal]', error instanceof Error ? error.message : String(error))
  process.exitCode = 1
})
